export function formatMethodLabel(method) {
    switch (method) {
        case 1:
            return 'Mendengar Audio'
            break;
        case 2:
            return 'Menulis Ulang'
            break;
        case 3:
            return 'Membaca'
            break;
        case 4:
            return 'Mengurut Kata'
            break;
        case 5:
            return 'Menebak Cepat'
            break;

        default:
            break;
    }
}